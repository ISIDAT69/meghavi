// Render Functions with Animation
function renderTableData(menuArray, tableBodyId, colsCount = 4) {
    const tbody = document.getElementById(tableBodyId);
    tbody.innerHTML = '';
    menuArray.forEach((row, idx) => {
        let tr = document.createElement('tr');
        // Add AOS fade-up animation with staggered delay for each row
        tr.setAttribute('data-aos', 'fade-up');
        tr.setAttribute('data-aos-delay', `${100 + idx * 35}`);
        for (let i = 0; i < colsCount; i++) {
            let td = document.createElement('td');
            td.textContent = row[i] || '';
            tr.appendChild(td);
        }
        tbody.appendChild(tr);
    });
    // Refresh AOS to animate new rows
    if (window.AOS) AOS.refresh();
}

